                        <p class="card-text" style="font-size: large;">Dilihat dari silsilahnya, tanaman Aglaonema merupakan tanaman yang masuk dalam Famili Araceae dengan Filum Plantae. Aglaonema Alam adalah jenis Aglaonema yang asli (bukan hasil silangan). Ciri utamanya adalah warna hijau pada daun yang dominan dan kombinasi hijau putih. Sedangkan Aglaonema hibrida (silangan) memiliki daun lebih berwarna-warni.
Aglaonema hasil silangan lokal, yaitu disilangkan di Indonesia. Silangan lokal pertama dan diakui dunia adalah Pride of Sumatra yang disilangkan oleh Gregori Hambali Garnadi seorang ahli Botani pada  tahun 1985. Ia menyilangkan Aglaonema Rotundum yang merupakan spesies Aglaonema asli Sumatera, dengan Aglaonema Commutatum yang berasal dari Filipina. 
Hasil dari persilangan tersebut didapatkan spesies baru yang terkenal sampai sekarang, yaitu Aglaonema “Pride of Sumatera”. Sejak ditemukan spesies baru ini, tanaman Aglaonema mulai digemari oleh banyak orang. Adapun jenis-jenis Aglaonema lainnya, seperti Aglaonema Pictum Tricolor, Silver Bay, Prestige, Silver Queen, Creta, Red Valentine, Red Anjamani, Maria, Moonlight, dan Bidadari.
